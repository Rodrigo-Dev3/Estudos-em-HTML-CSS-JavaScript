"use strict"

// + - * /
// ++ ou += (incremento) -- ou += (decremento)
// % (resto da divisão)

let numero1 = 1
numero1++ //irá incrementar em 1 o valor da variável
numero1 += 1 //operação semelhante a acima - porém, tem a oportunidade de inserir outros valores

let numero2 = 0
numero2-- //irá decrementar em 1 o valor da variável
numero2 -= 1 //operação semelhante a acima - porém, tem a oportunidade de inserir outros valores

console.log(numero1) 
console.log(numero2) 